// Простое хранилище в памяти (в реальном проекте это была бы база данных)
// При перезапуске сервера все добавленные статьи будут потеряны
let articles = [...initialArticles];

// Получение всех статей
export function getArticles(): Article[] {
  return [...articles];
}

// Получение статьи по ID
export function getArticleById(id: number): Article | undefined {
  return articles.find(article => article.id === id);
}

// Добавление новой статьи
export function addArticle(article: Omit<Article, 'id'>): Article {
  const newArticle: Article = {
    ...article,
    id: Date.now()
  };
  
  articles.push(newArticle);
  return newArticle;
}

// Обновление статьи
export function updateArticle(id: number, updates: Partial<Article>): Article | null {
  const index = articles.findIndex(article => article.id === id);
  
  if (index === -1) return null;
  
  articles[index] = { ...articles[index], ...updates };
  return articles[index];
}

// Удаление статьи
export function deleteArticle(id: number): boolean {
  const initialLength = articles.length;
  articles = articles.filter(article => article.id !== id);
  return articles.length < initialLength;
}
